import pandas as pd
import numpy as np

# Создаем DataFrame с данными
data = {
    'Имя': ['Алексей', 'Мария', 'Иван', 'Анна', 'Дмитрий', 'Ольга', 'Петр', 'Елена', 'Никита', 'Юлия'],
    'Математика': [5, 4, 3, 5, 4, 2, 5, 3, 4, 4],
    'Русский': [4, 5, 3, 4, 5, 4, 3, 4, 5, 4],
    'Английский': [3, 4, 5, 3, 4, 5, 3, 5, 4, 5],
    'Физика': [4, 3, 4, 5, 2, 3, 4, 5, 3, 4],
    'Химия': [5, 4, 3, 4, 5, 4, 5, 4, 3, 2]
}

df = pd.DataFrame(data)

# Выводим первые несколько строк DataFrame
print("Первые несколько строк DataFrame:")
print(df.head())

# Вычисляем среднюю оценку по каждому предмету
mean_scores = df[['Математика', 'Русский', 'Английский', 'Физика', 'Химия']].mean()
print("\nСредняя оценка по каждому предмету:")
print(mean_scores)

# Вычисляем медианную оценку по каждому предмету
median_scores = df[['Математика', 'Русский', 'Английский', 'Физика', 'Химия']].median()
print("\nМедианная оценка по каждому предмету:")
print(median_scores)

# Вычисляем Q1 и Q3 для оценок по математике
Q1_math = df['Математика'].quantile(0.25)
Q3_math = df['Математика'].quantile(0.75)
IQR_math = Q3_math - Q1_math
print("\nКвартили и интерквартильный размах для математики:")
print(f"Q1_math: {Q1_math}")
print(f"Q3_math: {Q3_math}")
print(f"IQR_math: {IQR_math}")

# Вычисляем стандартное отклонение по каждому предмету
std_devs = df[['Математика', 'Русский', 'Английский', 'Физика', 'Химия']].std()
print("\nСтандартное отклонение по каждому предмету:")
print(std_devs)
